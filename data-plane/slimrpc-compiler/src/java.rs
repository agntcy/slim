fn rpc_templates(client_streaming: bool, server_streaming: bool) -> &'static RpcTemplates {
    match (client_streaming, server_streaming) {
        (false, false) => &UNARY_UNARY_TEMPLATES,
        (false, true) => &UNARY_STREAM_TEMPLATES,
        (true, false) => &STREAM_UNARY_TEMPLATES,
        (true, true) => &STREAM_STREAM_TEMPLATES,
    }
}

fn apply(template: &str, vars: &[(&str, &str)]) -> String {
    let mut result = template.to_string();
    for (key, value) in vars {
        result = result.replace(key, value);
    }
    result
}

#[derive(Clone)]
struct JavaFileInfo {
    java_package: String,
    outer_class: String,
    multiple_files: bool,
}

fn file_name_base(file_name: &str) -> String {
    file_name
        .split('/')
        .next_back()
        .unwrap_or(file_name)
        .strip_suffix(".proto")
        .unwrap_or(file_name)
        .to_string()
}

fn java_file_info(file: &FileDescriptorProto) -> JavaFileInfo {
    let java_package = file
        .options
        .as_ref()
        .and_then(|opts| opts.java_package.clone())
        .filter(|s: &String| !s.is_empty())
        .or_else(|| file.package.clone())
        .unwrap_or_default();

    let base_name = file
        .name
        .as_deref()
        .map(file_name_base)
        .unwrap_or_else(|| "unknown_proto".to_string());

    let outer_class = file
        .options
        .as_ref()
        .and_then(|opts| opts.java_outer_classname.clone())
        .filter(|s: &String| !s.is_empty())
        .unwrap_or_else(|| format!("{}Proto", base_name.to_upper_camel_case()));

    let multiple_files = file
        .options
        .as_ref()
        .and_then(|opts| opts.java_multiple_files)
        .unwrap_or(false);

    JavaFileInfo {
        java_package,
        outer_class,
        multiple_files,
    }
}

fn collect_message_types(
    proto_prefix: String,
    java_prefix: String,
    messages: &[DescriptorProto],
    type_map: &mut HashMap<String, String>,
) {
    for msg in messages {
        let Some(name) = msg.name.as_ref() else {
            continue;
        };

        let proto_full = if proto_prefix.is_empty() {
            format!(".{}", name)
        } else {
            format!(".{}.{}", proto_prefix, name)
        };

        let java_full = if java_prefix.is_empty() {
            name.to_string()
        } else {
            format!("{}.{}", java_prefix, name)
        };

        type_map.insert(proto_full.clone(), java_full.clone());

        let nested_proto_prefix = if proto_prefix.is_empty() {
            name.to_string()
        } else {
            format!("{}.{}", proto_prefix, name)
        };

        collect_message_types(nested_proto_prefix, java_full, &msg.nested_type, type_map);
    }
}

fn resolve_java_type(
    full_type_name: &str,
    current_java_package: &str,
    type_map: &HashMap<String, String>,
) -> String {
    let trimmed = full_type_name.trim_start_matches('.');

    if trimmed.starts_with("google.protobuf.") {
        let type_name = trimmed.rsplit('.').next().unwrap_or(trimmed);
        return format!("com.google.protobuf.{}", type_name);
    }

    let lookup_key = format!(".{}", trimmed);
    if let Some(java_full) = type_map.get(&lookup_key) {
        if !current_java_package.is_empty() {
            let prefix = format!("{}.", current_java_package);
            if java_full.starts_with(&prefix) {
                return java_full.trim_start_matches(&prefix).to_string();
            }
        }
        return java_full.clone();
    }

    trimmed.rsplit('.').next().unwrap_or(trimmed).to_string()
}

/// Generate Java slimrpc code from a CodeGeneratorRequest
pub fn generate(request: CodeGeneratorRequest) -> Result<CodeGeneratorResponse> {
    let mut response = CodeGeneratorResponse::default();

    let mut file_map: HashMap<String, FileDescriptorProto> = HashMap::new();
    let mut file_info_map: HashMap<String, JavaFileInfo> = HashMap::new();

    for file in &request.proto_file {
        if let Some(name) = &file.name {
            file_map.insert(name.clone(), file.clone());
            file_info_map.insert(name.clone(), java_file_info(file));
        }
    }

    let mut type_map: HashMap<String, String> = HashMap::new();

    for file in &request.proto_file {
        let Some(file_name) = file.name.as_ref() else {
            continue;
        };
        let info = file_info_map
            .get(file_name.as_str())
            .cloned()
            .unwrap_or_else(|| java_file_info(file));

        let java_prefix = if info.multiple_files {
            info.java_package.clone()
        } else if info.java_package.is_empty() {
            info.outer_class.clone()
        } else {
            format!("{}.{}", info.java_package, info.outer_class)
        };

        let proto_prefix = file.package.clone().unwrap_or_default();
        collect_message_types(proto_prefix, java_prefix, &file.message_type, &mut type_map);
    }

    for file_name in &request.file_to_generate {
        let file_descriptor = file_map
            .remove(file_name)
            .context("File descriptor not found")?;

        let info = file_info_map
            .remove(file_name)
            .unwrap_or_else(|| java_file_info(&file_descriptor));

        let java_package = info.java_package;
        let package_decl = if java_package.is_empty() {
            String::new()
        } else {
            format!("package {};", java_package)
        };

        let package_name = file_descriptor.package.clone().unwrap_or_default();

        for service in file_descriptor.service {
            let service_name = service.name.context("Service name missing")?;
            let full_service_name = if package_name.is_empty() {
                service_name.clone()
            } else {
                format!("{}.{}", package_name, service_name)
            };

            let mut client_methods = String::new();
            let mut client_method_impls = String::new();
            let mut server_methods = String::new();
            let mut unimplemented_methods = String::new();
            let mut register_methods = String::new();
            let mut handler_impls = String::new();
            let mut group_client_methods = String::new();
            let mut group_client_method_impls = String::new();

            for method in service.method {
                let method_name = method.name.as_deref().context("Method name missing")?;
                let input_type = method.input_type.as_deref().context("Input type missing")?;
                let output_type = method
                    .output_type
                    .as_deref()
                    .context("Output type missing")?;

                let input_java = resolve_java_type(input_type, &java_package, &type_map);
                let output_java = resolve_java_type(output_type, &java_package, &type_map);

                let t = rpc_templates(
                    method.client_streaming.unwrap_or(false),
                    method.server_streaming.unwrap_or(false),
                );
                let vars = &[
                    ("{{METHOD_NAME}}", method_name),
                    ("{{SERVICE_NAME}}", service_name.as_str()),
                    ("{{INPUT_TYPE}}", input_java.as_str()),
                    ("{{OUTPUT_TYPE}}", output_java.as_str()),
                ];

                client_methods.push_str(&apply(t.client_method, vars));
                client_method_impls.push_str(&apply(t.client_impl, vars));
                server_methods.push_str(&apply(t.server_method, vars));
                unimplemented_methods.push_str(&apply(t.unimplemented, vars));
                register_methods.push_str(&apply(t.register, vars));
                handler_impls.push_str(&apply(t.handler, vars));
                group_client_methods.push_str(&apply(t.group_client_method, vars));
                group_client_method_impls.push_str(&apply(t.group_client_impl, vars));
            }

            let service_content = SERVICE_TEMPLATE
                .replace("{{SERVICE_NAME}}", &service_name)
                .replace("{{FULL_SERVICE_NAME}}", &full_service_name)
                .replace("{{CLIENT_METHODS}}", &client_methods)
                .replace("{{CLIENT_METHOD_IMPLS}}", &client_method_impls)
                .replace("{{SERVER_METHODS}}", &server_methods)
                .replace("{{UNIMPLEMENTED_METHODS}}", &unimplemented_methods)
                .replace("{{REGISTER_METHODS}}", register_methods.trim_end())
                .replace("{{HANDLER_IMPLS}}", &handler_impls)
                .replace("{{GROUP_CLIENT_METHODS}}", &group_client_methods)
                .replace("{{GROUP_CLIENT_METHOD_IMPLS}}", &group_client_method_impls);

            let output_file_name = format!("{}Slimrpc.java", service_name);
            let output_path = if java_package.is_empty() {
                output_file_name
            } else {
                format!("{}/{}", java_package.replace('.', "/"), output_file_name)
            };

            let final_content = FILE_TEMPLATE
                .replace("{{PROTO_FILE}}", file_name)
                .replace("{{PACKAGE_DECL}}", &package_decl)
                .replace("{{SERVICE_DEFINITION}}", &service_content);

            response.file.push(File {
                name: Some(output_path),
                content: Some(final_content),
                ..Default::default()
            });
        }
    }

    Ok(response)
}

